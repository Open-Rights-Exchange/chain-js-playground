export class ERC20TransferFrom_raw implements ITransactionBuilder {
    async build(chain: Chain, options: IOptionBag, txnType: ETHTxnTypes): Promise<TransactionBuilderResponse> {

        const defaultTxOptions = { gasLimit: 91000 }
        var transaction : Transaction = await chain.new.Transaction(defaultTxOptions)

        const action: any = {
            from: options.fromAccountName,
            to: options.toAccountName,
            contract: {
              abi: ERC20ABI,
              parameters: ['0xa26f803334002cbd7ddee757e95e684e417901e9', 1],
              method: 'transfer',
            },
          }
    
        return {transaction, action}
    }
}